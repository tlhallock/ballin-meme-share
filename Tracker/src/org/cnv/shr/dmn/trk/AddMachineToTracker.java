    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String url = jTextField1.getText();
        String name = jTextField2.getText();
        String ident = jTextField3.getText();
        int beginPort = (Integer) jSpinner1.getValue();
        int endPort = (Integer) jSpinner2.getValue();
        if (endPort < beginPort +1)
        {
            JOptionPane.showMessageDialog(this, "Begin port must be less then end port.", "Begin port must be less then end port.", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // add here...
        MachineEntry entry = new MachineEntry(ident, "", url, beginPort, name);
        store.machineFound(entry, System.currentTimeMillis());
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

