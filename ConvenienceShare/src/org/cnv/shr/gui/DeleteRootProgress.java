    private int soFar;
    public void begin(int step, int total)
    {
			jProgressBar1.setMaximum(messages.length);
			jProgressBar1.setValue(step);
			jLabel3.setText(messages[step]);
			jProgressBar2.setValue(soFar = 0);
			jProgressBar2.setMaximum(total);
    }
    
    public void updateProgress(int numDeleted)
    {
        jProgressBar2.setValue(Math.min(soFar += numDeleted, jProgressBar2.getMaximum()));
        repaint();
    }
    
